<h2>What's Next?</h2>
<h4><span style="text-decoration: underline;">More Stats</span></h4>
<p>As mentioned above, and once I start getting the game box scores in I'll be adding in single game stats to the stat search pages.&nbsp;</p>
<h4><span style="text-decoration: underline;">Game Search</span></h4>
<p>A game search feature similar to the stat search where you'll be able to quickly find the largest loss on the road to a conference opponent on a Tuesday.</p>
<h4><span style="text-decoration: underline;">Views by Opponent, Place, or Person</span></h4>
<p>All games, records, and stats Vs an opponent, in an arena, city, state, or involving a person with a click.</p>
<h4><span style="text-decoration: underline;">Women's Stats</span></h4>
<p>Women's basketball belongs here too and I plan on bringing them here. With that will be the ability to include them in all of the above searches and views.</p>
