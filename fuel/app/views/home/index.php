<h1>Welcome to Racerhistory.com!</h1>
<p>There has been some major changes around here, a complete overhaul as a matter of fact. 
You can still find all of the player stats and season history you could before, but in (what I hope) is a more pleasant way of interacting with it and with greater detail than ever before!&nbsp;</p>
