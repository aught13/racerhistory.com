<p>What else is new is something that you won't see. With the overhaul came the creation of an administrative backend that will make it much easier to keep things up to date, and add additional content not previously available.</p>
<h2>What's Missing?</h2>
<h4><span style="text-decoration: underline;">Images, stories, and biographies. </span></h4>
